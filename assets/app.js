async function loadResultJSON() {
  const rawEl = document.getElementById('raw_json');
  const link = document.getElementById('download_link');
  try {
    const res = await fetch('result.json', { cache: 'no-store' });
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    const data = await res.json();

    // Fill metrics
    document.getElementById('row_count').textContent = data.row_count ?? '—';
    document.getElementById('regions').textContent = data.regions ?? '—';

    // Top products table
    const tpBody = document.querySelector('#top_products tbody');
    tpBody.innerHTML = '';
    (data.top_n_products_by_revenue || []).forEach(row => {
      const tr = document.createElement('tr');
      const tdP = document.createElement('td');
      const tdR = document.createElement('td');
      tdP.textContent = row.product;
      tdR.textContent = new Intl.NumberFormat(undefined, { style: 'currency', currency: 'USD' }).format(row.revenue);
      tr.append(tdP, tdR);
      tpBody.appendChild(tr);
    });

    // Rolling table
    const rBody = document.querySelector('#rolling tbody');
    rBody.innerHTML = '';
    const rolling = data.rolling_7d_revenue_by_region || {};
    Object.keys(rolling).sort().forEach(region => {
      const tr = document.createElement('tr');
      const tdRegion = document.createElement('td');
      const tdVal = document.createElement('td');
      tdRegion.textContent = region;
      tdVal.textContent = new Intl.NumberFormat(undefined, { style: 'currency', currency: 'USD' }).format(rolling[region]);
      tr.append(tdRegion, tdVal);
      rBody.appendChild(tr);
    });

    rawEl.textContent = JSON.stringify(data, null, 2);
    link.style.display = '';
  } catch (err) {
    rawEl.textContent = `Could not load result.json yet (it is generated by CI): ${err.message}.\n` +
      `If you just pushed, wait for the GitHub Actions workflow to finish, then refresh.`;
    link.style.display = 'none';
  }
}

window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('refresh_btn').addEventListener('click', loadResultJSON);
  loadResultJSON();
});
